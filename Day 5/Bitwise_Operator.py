T = 10
F = 20


print("T & F is",T & F)     # bitwise and
print("T | F is", T | F)     # bitwise or
print("T ^ F is",T ^ F)     # bitwise xor
print("~T is",~T)           # bitwise not
print("~F is",~F)           # bitwise not
print("T << 2 is",T << 2)   # bitwise left shift
print("F >> 2 is",F >> 2)   # bitwise right shift
print("F << 2 is",F << 2)   # bitwise left shift
print("T >> 2 is",T >> 2)   # bitwise right shift