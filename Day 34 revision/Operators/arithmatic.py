var = 2//2  # floor division gives quotient value without decimal
print(var)
var = 2/2   # Divsion gives quotient with decimal
print(var)
var = 2-2   # Subtraction
print(var)
var = 2+2   # Addition
print(var)
var = 2*2   # Multiplication
print(var)
var = 2%2   # Modulus
print(var)
var = 2**3  # Exponential (power 3 on 2 = 8(2*2*2))
print(var)